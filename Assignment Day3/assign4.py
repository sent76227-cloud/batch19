'''
 Area of Rectangle

Write a Python program that:

Accepts length and breadth.
Calculates area.

Input:
Length = 10
Breadth = 5

Output:
Area = 50
'''
print("Accepts length and breadth.")
print("Calculates area.")
len = int(input("Enter Length: "))
bre = int(input("Enter Breadth: "))
area = len * bre
print(f"Area = {area}")